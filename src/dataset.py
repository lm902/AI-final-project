# ── Per-dataset normalization constants (from 01_EDA_Binger.ipynb) ──────────
DATASET_STATS = {
    'COD10K': {
        'mean': (0.407, 0.424, 0.340),
        'std':  (0.208, 0.198, 0.194),
    },
    'ACD1K': {
        'mean': (0.411, 0.405, 0.327),
        'std':  (0.196, 0.196, 0.184),
    },
    'CAMO': {
        'mean': (0.479, 0.461, 0.360),
        'std':  (0.197, 0.195, 0.185),
    },
    'JOINT': {
        'mean': (0.432, 0.430, 0.342),
        'std':  (0.200, 0.196, 0.188),
    },
}

INPUT_SIZE = 512


# ──────────────────────────────────────────────────────────────────────────────
# JSON split loader
# ──────────────────────────────────────────────────────────────────────────────

def load_split_filenames(splits_dir, dataset, split):
    """
    Load filenames for a given dataset and split from JSON index files.

    Args:
        splits_dir (str): Path to splits/ folder.
        dataset    (str): 'acd1k' or 'cod10k'.
        split      (str): 'train', 'val', or 'test'.

    Returns:
        list of filenames (strings, not full paths)
    """
    splits_dir = Path(splits_dir)
    json_path  = splits_dir / f'{dataset.lower()}_splits.json'

    if not json_path.exists():
        raise FileNotFoundError(
            f"Split file not found: {json_path}\n"
            f"Run src/generate_splits.py first."
        )

    with open(json_path) as f:
        data = json.load(f)

    if split not in data:
        raise KeyError(
            f"Split '{split}' not found in {json_path}. "
            f"Available: {list(data.keys())}"
        )

    filenames = data[split]
    print(f"  [Splits] {dataset.upper()} {split}: {len(filenames)} images "
          f"(from {json_path.name})")
    return filenames


def load_holdout_filenames(splits_dir, holdout_name):
    """
    Load filenames for a hold-out set from JSON index files.

    Args:
        splits_dir   (str): Path to splits/ folder.
        holdout_name (str): 'acd1k', 'cod10k', or 'noise'.

    Returns:
        list of filenames (strings, not full paths)
    """
    splits_dir = Path(splits_dir)
    json_path  = splits_dir / f'hold_out_{holdout_name.lower()}.json'

    if not json_path.exists():
        raise FileNotFoundError(
            f"Hold-out file not found: {json_path}\n"
            f"Run src/generate_splits.py first."
        )

    with open(json_path) as f:
        data = json.load(f)

    filenames = data.get('files', [])
    print(f"  [Hold-out] {holdout_name.upper()}: {len(filenames)} images")
    return filenames


# ──────────────────────────────────────────────────────────────────────────────
# Augmentation pipelines
# ──────────────────────────────────────────────────────────────────────────────

def get_train_transforms(mean, std, input_size=INPUT_SIZE):
    """Training augmentation — geometric transforms applied to both
    image and mask; colour jitter applied to image only."""
    return A.Compose([
        A.Resize(input_size, input_size),
        A.HorizontalFlip(p=0.5),
        A.Rotate(limit=15, p=0.4),
        A.ColorJitter(
            brightness=0.2,
            contrast=0.2,
            saturation=0.1,
            hue=0.05,
            p=0.4
        ),
        A.Normalize(mean=mean, std=std),
        ToTensorV2(),
    ])


def get_val_transforms(mean, std, input_size=INPUT_SIZE):
    """Validation/test pipeline — resize and normalize only."""
    return A.Compose([
        A.Resize(input_size, input_size),
        A.Normalize(mean=mean, std=std),
        ToTensorV2(),
    ])


# ──────────────────────────────────────────────────────────────────────────────
# Dataset class
# ──────────────────────────────────────────────────────────────────────────────

class CamouflageDataset(Dataset):
    """
    Generic camouflage segmentation dataset.

    Can be initialised in two modes:
      1. file_list mode (recommended): pass a list of filenames loaded
         from a JSON split file — only those files are loaded.
      2. folder scan mode (fallback): scans image_dir for all images.

    Args:
        image_dir    (str)        : Path to folder containing images.
        mask_dir     (str)        : Path to folder containing binary masks.
        transform                 : Albumentations transform pipeline.
        dataset_name (str)        : Label ('COD10K', 'ACD1K', 'CAMO').
        file_list    (list | None): List of filenames to load. If None,
                                    all images in image_dir are loaded.
    """

    IMG_EXTS = {'.jpg', '.jpeg', '.png', '.bmp'}

    def __init__(self, image_dir, mask_dir, transform=None,
                 dataset_name='', file_list=None):
        self.image_dir    = Path(image_dir)
        self.mask_dir     = Path(mask_dir)
        self.transform    = transform
        self.dataset_name = dataset_name

        if file_list is not None:
            # ── JSON split mode: load only listed files ──
            self.image_paths = []
            missing = []
            for fname in file_list:
                p = self.image_dir / fname
                if p.exists():
                    self.image_paths.append(p)
                else:
                    missing.append(fname)
            if missing:
                print(f"  ⚠️  [{dataset_name}] {len(missing)} files "
                      f"listed in split not found on disk: "
                      f"{missing[:3]}{'...' if len(missing) > 3 else ''}")
        else:
            # ── Folder scan mode: load all images in directory ──
            self.image_paths = sorted([
                p for p in self.image_dir.iterdir()
                if p.suffix.lower() in self.IMG_EXTS
            ])

        if len(self.image_paths) == 0:
            raise FileNotFoundError(
                f"No images found for [{dataset_name}] in {image_dir}."
            )

        # Verify at least one mask exists
        sample_mask = self._get_mask_path(self.image_paths[0])
        if not sample_mask.exists():
            raise FileNotFoundError(
                f"Mask not found: {sample_mask}"
            )

        mode = "split" if file_list is not None else "scan"
        print(f"  [{dataset_name}] {len(self.image_paths)} images "
              f"({mode} mode)")

    def _get_mask_path(self, img_path):
        """Return the corresponding mask path for an image path."""
        mask_path = self.mask_dir / (img_path.stem + '.png')
        if not mask_path.exists():
            mask_path = self.mask_dir / (img_path.stem + '.jpg')
        return mask_path

    def __len__(self):
        return len(self.image_paths)

    def __getitem__(self, idx):
        img_path  = self.image_paths[idx]
        mask_path = self._get_mask_path(img_path)

        # Load image as RGB
        image = np.array(Image.open(img_path).convert('RGB'))

        # Load and binarize mask
        if mask_path.exists():
            mask = np.array(Image.open(mask_path).convert('L'))
        else:
            mask = np.zeros(
                (image.shape[0], image.shape[1]), dtype=np.uint8
            )

        mask = (mask > 127).astype(np.uint8)

        if self.transform:
            augmented = self.transform(image=image, mask=mask)
            image = augmented['image']
            mask  = augmented['mask']

        mask = mask.float().unsqueeze(0)    # [1, H, W]

        return {
            'image':    image,
            'mask':     mask,
            'filename': img_path.name,
            'dataset':  self.dataset_name,
        }


