def build_dataset(data_root, condition='joint', split='train',
                  splits_dir='splits'):
    """
    Build a dataset for a given experimental condition and split.

    For ACD1K and COD10K, filenames are loaded from JSON split index files
    to guarantee identical splits across all three experiments.
    CAMO uses its original folder structure (no split file needed).

    Args:
        data_root  (str): Root directory containing all dataset folders.
        condition  (str): 'cod10k', 'acd1k', or 'joint'.
        split      (str): 'train', 'val', or 'test'.
        splits_dir (str): Path to splits/ folder with JSON index files.

    Returns:
        CamouflageDataset or ConcatDataset
    """
    data_root  = Path(data_root)
    splits_dir = Path(splits_dir)
    condition  = condition.lower()

    # ── Path definitions ─────────────────────────────────────────────────────
    PATHS = {
        'COD10K': {
            'images': data_root / 'COD10K-v3/Train/Image',
            'masks':  data_root / 'COD10K-v3/Train/GT_Object',
        },
        'ACD1K': {
            'train': {
                'images': data_root / 'dataset-splitM/Training/images',
                'masks':  data_root / 'dataset-splitM/Training/GT',
            },
            'test': {
                'images': data_root / 'dataset-splitM/Testing/images',
                'masks':  data_root / 'dataset-splitM/Testing/GT',
            },
        },
        'CAMO': {
            'train': {
                'images': data_root / 'CAMO-V.1.0-CVIU2019/Images/Train',
                'masks':  data_root / 'CAMO-V.1.0-CVIU2019/GT',
            },
            'test': {
                'images': data_root / 'CAMO-V.1.0-CVIU2019/Images/Test',
                'masks':  data_root / 'CAMO-V.1.0-CVIU2019/GT',
            },
        },
    }

    # ── Normalization stats ───────────────────────────────────────────────────
    stats_key = 'JOINT' if condition == 'joint' else condition.upper()
    stats     = DATASET_STATS[stats_key]

    # ── Transforms ───────────────────────────────────────────────────────────
    if split == 'train':
        transform = get_train_transforms(stats['mean'], stats['std'])
    else:
        transform = get_val_transforms(stats['mean'], stats['std'])

    # ── COD10K only (Experiment 1) ────────────────────────────────────────────
    if condition == 'cod10k':
        # Use official test folder for test split; JSON splits for train/val
        if split == 'test':
            return CamouflageDataset(
                data_root / 'COD10K-v3/Test/Image',
                data_root / 'COD10K-v3/Test/GT_Object',
                transform=transform,
                dataset_name='COD10K',
                file_list=None,  # use all official test images
            )
        else:
            file_list = load_split_filenames(splits_dir, 'cod10k', split)
            return CamouflageDataset(
                PATHS['COD10K']['images'],
                PATHS['COD10K']['masks'],
                transform=transform,
                dataset_name='COD10K',
                file_list=file_list,
            )

    # ── ACD1K only (Experiment 2) ─────────────────────────────────────────────
    elif condition == 'acd1k':
        # Use official test folder for test split; JSON splits for train/val
        if split == 'test':
            return CamouflageDataset(
                PATHS['ACD1K']['test']['images'],
                PATHS['ACD1K']['test']['masks'],
                transform=transform,
                dataset_name='ACD1K',
                file_list=None,  # use all official test images
            )
        else:
            file_list = load_split_filenames(splits_dir, 'acd1k', split)
            return CamouflageDataset(
                PATHS['ACD1K']['train']['images'],
                PATHS['ACD1K']['train']['masks'],
                transform=transform,
                dataset_name='ACD1K',
                file_list=file_list,
            )

    # ── Joint training (Experiment 3) ────────────────────────────────────────
    elif condition == 'joint':
        datasets = []

        # COD10K — JSON splits for train/val, official test for test
        if split == 'test':
            cod10k_ds = CamouflageDataset(
                data_root / 'COD10K-v3/Test/Image',
                data_root / 'COD10K-v3/Test/GT_Object',
                transform=transform,
                dataset_name='COD10K',
                file_list=None,
            )
        else:
            cod10k_files = load_split_filenames(splits_dir, 'cod10k', split)
            cod10k_ds = CamouflageDataset(
                PATHS['COD10K']['images'],
                PATHS['COD10K']['masks'],
                transform=transform,
                dataset_name='COD10K',
                file_list=cod10k_files,
            )
        datasets.append(cod10k_ds)

        # CAMO — use original folder structure (no split file)
        camo_split = 'train' if split in ('train', 'val') else 'test'
        camo_ds = CamouflageDataset(
            PATHS['CAMO'][camo_split]['images'],
            PATHS['CAMO'][camo_split]['masks'],
            transform=transform,
            dataset_name='CAMO',
            file_list=None,
        )
        datasets.append(camo_ds)

        # ACD1K — JSON splits for train/val, official test for test
        if split == 'test':
            acd1k_ds = CamouflageDataset(
                PATHS['ACD1K']['test']['images'],
                PATHS['ACD1K']['test']['masks'],
                transform=transform,
                dataset_name='ACD1K',
                file_list=None,
            )
        else:
            acd1k_files = load_split_filenames(splits_dir, 'acd1k', split)
            acd1k_ds = CamouflageDataset(
                PATHS['ACD1K']['train']['images'],
                PATHS['ACD1K']['train']['masks'],
                transform=transform,
                dataset_name='ACD1K',
                file_list=acd1k_files,
            )
        datasets.append(acd1k_ds)

        return torch.utils.data.ConcatDataset(datasets)

    else:
        raise ValueError(
            f"Unknown condition '{condition}'. "
            f"Choose from: 'cod10k', 'acd1k', 'joint'."
        )


def build_holdout_dataset(data_root, holdout_name, splits_dir='splits'):
    """
    Build a dataset from a hold-out JSON index file for final evaluation.

    Args:
        data_root    (str): Root data directory.
        holdout_name (str): 'acd1k', 'cod10k', or 'noise'.
        splits_dir   (str): Path to splits/ folder.

    Returns:
        CamouflageDataset
    """
    data_root  = Path(data_root)
    splits_dir = Path(splits_dir)

    file_list = load_holdout_filenames(splits_dir, holdout_name)

    if holdout_name == 'acd1k':
        image_dir = data_root / 'dataset-splitM/Training/images'
        mask_dir  = data_root / 'dataset-splitM/Training/GT'
        stats     = DATASET_STATS['ACD1K']
    elif holdout_name == 'cod10k':
        image_dir = data_root / 'COD10K-v3/Train/Image'
        mask_dir  = data_root / 'COD10K-v3/Train/GT_Object'
        stats     = DATASET_STATS['COD10K']
    elif holdout_name == 'noise':
        image_dir = data_root / 'COD10K-v3/Train/Image'
        mask_dir  = data_root / 'COD10K-v3/Train/GT_Object'
        stats     = DATASET_STATS['COD10K']
    else:
        raise ValueError(f"Unknown holdout_name '{holdout_name}'.")

    transform = get_val_transforms(stats['mean'], stats['std'])

    return CamouflageDataset(
        image_dir, mask_dir,
        transform=transform,
        dataset_name=f'HOLDOUT_{holdout_name.upper()}',
        file_list=file_list,
    )


